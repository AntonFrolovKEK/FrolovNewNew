package Kniga;

public class Circle {
    private Newww centre;
    private double radius;

    public Circle(Newww centre, double radius) {
        this.centre = centre;
        this.radius = radius;
    }

    public Newww getCentre() {
        return centre;
    }

    public double getRadius() {
        return radius;
    }

    public void setCentre(Newww centre) {
        this.centre = centre;
    }

    public void setRadius(double radius) {
        this.radius = radius;
    }
}

